public class Point {

    private double x;
    private double y;

    public Point ( double pX, double pY ) {

        x = pX;
        y = pY;

    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void changeX ( double pX ) {

        x = pX;

    }

    public void changeY ( double pY ) {

        y = pY;

    }


}

